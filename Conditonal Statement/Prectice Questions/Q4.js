/*Q4. A String is a golden string is it start with the character 'A' or 'a' and has a total length greater then 5.
        For a given string print if ir golden or not*/

let value = 'apples';

if ((value[0] === 'a' || value[0] === 'A') && (value.length > 5))
{
    console.log("Golden String");
}
else
{
    console.log("Not a Golden String");
}

