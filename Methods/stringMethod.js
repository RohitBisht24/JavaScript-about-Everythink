//ToUpperCase
let str2 = "hello world";
console.log("To Upper Case : ",str2.toUpperCase());  //output : "HELLO WORLD"

//ToLowerCase
let str3 = "HELLO WORLD";
console.log("To Lower Case : ",str3.toLowerCase());  //output : "hello world"





